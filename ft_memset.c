#include"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)b;
	i = 0;
	while (i < len)
	{
		p[i] = c;
		i++;
	}
	return (b);
}
int main(void){
	int a[] = {1, 2};
	ft_memset(a, 1, 2);
	printf("%d", a[0]);
}